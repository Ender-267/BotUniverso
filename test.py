def convert_hash_format(hash_string):
    # Define a regex pattern to match the hash in the form $SHA256$<hash>@<salt>
    pattern = r'\$SHA256\$([a-fA-F0-9]+)@(.+)'
    match = re.search(pattern, hash_string)

    if match:
        hash_value = match.group(1)
        salt_value = match.group(2)
        converted_hash = f"{hash_value}:{salt_value}"
        
        return converted_hash
    else:
        return None
    
